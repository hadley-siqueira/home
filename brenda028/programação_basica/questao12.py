print( " >> Calculadora de lucros << ")

a = int(input( " Informe a quantidade de camisetas pequenas: " ))
b = int(input( " Informe a quantidade de camisetas médias: " ))
c = int(input( " Informe a quantidade de camisetas grandes: " ))

wa = a * 10
wb = b * 12
wc = c * 15
ww = wa + wb + wc

print( " O valor arrecadado é: " + str(ww))

