n1 = int(input("Digite o primeiro número:"))
n2 = int(input("Digite o segundo número:"))
n3 = int(input("Digite o terceiro número:"))

if n2 + n3==n1:
    print("A soma dos dois ultimos números é igual ao primeiro número")
else:
    print("A soma dos dois ultimos números NÃO é igual ao primeiro número")
