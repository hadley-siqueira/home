x = float(input("Digite um numero: "))
y = float(input("Digite mais numero: "))
z = float(input("Digite outro numero: "))

if z + y == x: 
    print("A soma de " +str(z)+ " e " + str(y) + " resulta em " + str(x))
else:
    print("Os numeros " + str(z) + " e " + str(y) + " nao resultam em " + str(x))
 
