b1 = int(input("Informe o numero: "))
b2 = int(input("Informe o segundo numero: "))
b3 = int(input("Informe o terceiro numero:"))
resultado = b1 + b2

if resultado == b3:
    print("O resultado da soma é identico ao ultimo numero")
else:
    print("O resultado da soma é diferente ao ultimo numero")
