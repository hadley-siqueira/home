k1 = int(input("Digite o primeiro número: "))
k2 = int(input("Digite o segundo número: "))
k3 = int(input("Digite o terceiro número: "))


if k1 + k2 == k3:
    print("Os dois primeros números somados resultam no terceiro número")

else:
    print("Os dois primeiros números somados não resultam no terceiro número") 
