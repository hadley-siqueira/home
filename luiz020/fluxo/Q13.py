k = int(input("Digite o horário: "))

if k == 1:
    print("13:00 - 13:45")
elif k == 2:
    print("13:45 - 14:30")
elif k == 3:
    print("14:50 - 15:35")
elif k == 4:
    print("15:35 - 16:20")
elif k == 5:
    print("16:30 - 17:15")
elif k == 6:
    print("17:15 - 18:00")
else: 
    print("Entrada inválida")

